/**
 * @function	square
 * @description Returns the square of a number
 * @param 		{Number} number The number you wish to find the square of
 * @returns		{Number} The sqaure of the number argument
 * @example 	maths.square ( 2 ) // 4
 */
export default function ( number ) {
	 
	return number * number; 
}